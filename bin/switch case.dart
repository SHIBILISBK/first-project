void main(){
  var size = 'm';
  switch(size){
    case'xs':
    print('my shirt size is xs');
    break;
    case's':
      print('my shirt size is s');
    break;
    case'm':
    print('my shirt size is m');
    break;
    case'l':
    print('my shirt size is l');
    break;
    case'xl':
    print('my shirt size is xl');
    break;
    case'xxl':
    print('my shirt size is xxl');
    break;
    default :
      print('none of these');
  }
}